function isBalanced(s: string): string {
  // Write your code here
  if (s.length % 2 !== 0) return "NO";
  const ar = s.split("");
  const arr = [];
  const arr2 = [];
  let x = 0;

  for (const ele of ar) {
    if (ele === "(" || ele === "[" || ele === "{") {
      arr.push(ele);
    } else {
      const newEle = arr.pop() + ele;
      if (newEle !== "[]" && newEle !== "{}" && newEle !== "()") {
        return "NO";
      }
    }
  }
  if (arr.length > 0) return "NO";

  return "YES";
}

